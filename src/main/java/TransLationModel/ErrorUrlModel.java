package TransLationModel;

public class ErrorUrlModel {
	private int Id;
	private String URL;
	private int urlType;
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	}
	public String getURL() {
		return URL;
	}
	public void setURL(String url) {
		URL = url;
	}
	
	
	public int getUrlType() {
		return urlType;
	}
	public void setUrlType(int urlType) {
		this.urlType = urlType;
	}

}
