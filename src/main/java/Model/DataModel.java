package Model;

public class DataModel {
	private String name;
	private String time;
	private String hour;
	private String data;
	private String url;
	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getTime() {
		return time;
	}
	
	public void setTime(String time) {
		this.time = time;
	}
	
	public String getHour() {
		return this.hour;
	}
	
	public void setHour(String hour2) {
		this.hour = hour2;
	}
	public String getData() {
		return this.data;
	}
	
	public void setData(String data) {
		this.data = data;
	}
	
	public String getUrl() {
		return this.url;
	}

	public void setUrl(String url) {
		this.url = url;
	}


	

}
